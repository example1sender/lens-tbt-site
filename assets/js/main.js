/* =====================================================================
   The Lens Perspective Photography — Global interactions
   Lens cursor · scroll reveal · testimonial slider · parallax ·
   gallery filter · form validation. All feature-detected & no-throw.
   ===================================================================== */
(function () {
  "use strict";

  var reduceMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
  var canHover = window.matchMedia("(hover: hover) and (pointer: fine)").matches;

  /* ----------------------------------------- managed images + text (admin) */
  // Two layers, lowest to highest priority:
  //   1. PUBLISHED (assets/js/published.json) — global, seen by EVERY visitor
  //      and device once redeployed. Generated by admin.html "Publish".
  //   2. localStorage overrides — this browser only (live preview while editing).
  var PUB = { images: {}, text: {}, galleries: {} };

  function mergedImages() {
    var ls = (typeof window.TLP_getOverrides === "function") ? (window.TLP_getOverrides() || {}) : {};
    var o = {};
    Object.keys(PUB.images || {}).forEach(function (k) { o[k] = PUB.images[k]; });
    Object.keys(ls).forEach(function (k) { o[k] = ls[k]; });
    return o;
  }
  function mergedText() {
    var ls = (typeof window.TLP_getText === "function") ? (window.TLP_getText() || {}) : {};
    var o = {};
    Object.keys(PUB.text || {}).forEach(function (k) { o[k] = PUB.text[k]; });
    Object.keys(ls).forEach(function (k) { o[k] = ls[k]; });
    return o;
  }

  // Galleries: published collection wins unless this browser has a local edit.
  function mergedGalleries() {
    var ls = (typeof window.TLP_getGalleries === "function") ? (window.TLP_getGalleries() || {}) : {};
    var o = {};
    Object.keys(PUB.galleries || {}).forEach(function (k) { o[k] = PUB.galleries[k]; });
    Object.keys(ls).forEach(function (k) { o[k] = ls[k]; });
    return o;
  }
  function gEsc(s) { return String(s == null ? "" : s).replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;").replace(/"/g, "&quot;"); }

  /* ---- video support (YouTube/Vimeo links, .mp4 links, uploaded clips) ---- */
  function ytId(u) { var m = String(u).match(/(?:youtube\.com\/(?:watch\?v=|embed\/|v\/|shorts\/)|youtu\.be\/)([\w-]{6,})/); return m ? m[1] : null; }
  function vimeoId(u) { var m = String(u).match(/vimeo\.com\/(?:video\/)?(\d+)/); return m ? m[1] : null; }
  function isFileVideo(u) { return /\.(mp4|webm|ogg|mov)(\?|#|$)/i.test(u) || /[?&]id=[^&]*\.(mp4|webm|ogg|mov)/i.test(u); }
  function isVideo(u) { return !!(ytId(u) || vimeoId(u) || isFileVideo(u)); }
  function videoEmbed(u, aspect) {
    var ratio = aspect || "16/9";
    var yt = ytId(u), vm = vimeoId(u);
    if (yt) return '<div class="w-full" style="aspect-ratio:' + ratio + '"><iframe class="w-full h-full" src="https://www.youtube.com/embed/' + yt + '" loading="lazy" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe></div>';
    if (vm) return '<div class="w-full" style="aspect-ratio:' + ratio + '"><iframe class="w-full h-full" src="https://player.vimeo.com/video/' + vm + '" loading="lazy" frameborder="0" allow="autoplay; fullscreen; picture-in-picture" allowfullscreen></iframe></div>';
    return '<video class="w-full block" style="' + (aspect ? "aspect-ratio:" + aspect + ";object-fit:cover;height:100%" : "") + '" controls preload="metadata" playsinline src="' + gEsc(u) + '"></video>';
  }

  // Renders every [data-gallery="id"] container from its (dynamic) image list.
  //   data-gallery-aspect="4/5"  -> fixed-ratio grid tiles (default: natural / masonry)
  //   data-gallery-item="<classes>" -> extra classes per tile (e.g. masonry break-inside)
  //   data-empty="text" -> message shown when the gallery has no images yet
  function applyGalleries() {
    var g = mergedGalleries();
    document.querySelectorAll("[data-gallery]").forEach(function (c) {
      var id = c.getAttribute("data-gallery");
      var list = g[id] || [];
      var aspect = c.getAttribute("data-gallery-aspect");
      var itemCls = c.getAttribute("data-gallery-item") || "";
      if (!list.length) {
        var empty = c.getAttribute("data-empty");
        c.innerHTML = empty ? '<p class="col-span-full text-center text-on-surface-variant/70 py-16">' + gEsc(empty) + '</p>' : "";
        return;
      }
      c.innerHTML = list.map(function (it) {
        if (!it || !it.url) return "";
        var alt = gEsc(it.alt || "");
        if (isVideo(it.url)) {
          return '<figure class="' + itemCls + ' relative overflow-hidden rounded-xl m-0 bg-black">' + videoEmbed(it.url, aspect) + '</figure>';
        }
        var inner;
        if (aspect) {
          inner = '<div class="w-full overflow-hidden" style="aspect-ratio:' + gEsc(aspect) + '"><img src="' + gEsc(it.url) + '" alt="' + alt + '" loading="lazy" class="w-full h-full object-cover transition-transform duration-700 group-hover:scale-105"></div>';
        } else {
          inner = '<img src="' + gEsc(it.url) + '" alt="' + alt + '" loading="lazy" class="w-full block transition-transform duration-700 group-hover:scale-105">';
        }
        return '<figure class="' + itemCls + ' group relative overflow-hidden rounded-xl bento-hover m-0" data-cursor="View">' + inner + '</figure>';
      }).join("");
    });
  }

  // Applies merged image map over the page's default images.
  // Looks for [data-img] (<img>) and [data-bg] (bg div).
  // Special value "__removed__" hides the image entirely (no default).
  var IMG_REMOVED = "__removed__";
  function applyManagedImages() {
    var ov = mergedImages();
    document.querySelectorAll("[data-img]").forEach(function (el) {
      var v = ov[el.getAttribute("data-img")];
      if (v === IMG_REMOVED) { el.style.display = "none"; el.removeAttribute("src"); }
      else if (v) { el.style.display = ""; el.src = v; }
    });
    document.querySelectorAll("[data-bg]").forEach(function (el) {
      var v = ov[el.getAttribute("data-bg")];
      if (v === IMG_REMOVED) { el.style.backgroundImage = "none"; el.setAttribute("data-bg-removed", "1"); }
      else if (v) { el.style.backgroundImage = "url('" + v.replace(/'/g, "\\'") + "')"; el.removeAttribute("data-bg-removed"); }
    });
  }

  var PUBCACHE = "tlp_pub_cache";
  function applyPub(p) {
    if (p && typeof p === "object") {
      PUB = { images: p.images || {}, text: p.text || {}, galleries: p.galleries || {} };
      applyManagedImages();
      applyManagedText();
      applyGalleries();
    }
  }
  // Loads the published global layer so admin edits appear for EVERY visitor
  // on EVERY device, with no redeploy. Order:
  //   1. instant paint from last cached settings (no flash for returns)
  //   2. fresh from the serverless function (the live source of truth)
  //   3. fallback to the committed published.json (local preview / offline)
  function fetchPublished() {
    // 1) Instant paint from the last-known published settings (durable — survives
    //    a slow/cold server, so the site never flashes back to defaults).
    try { var c = JSON.parse(localStorage.getItem(PUBCACHE) || "null"); if (c) applyPub(c); } catch (e) {}
    // 2) Refresh from the server, with a generous timeout and one retry.
    //    CRITICAL: if the fetch fails, we keep whatever is already shown and
    //    NEVER fall back to built-in defaults. Only a real response updates things.
    function tryLoad(attempt) {
      var ctrl = ("AbortController" in window) ? new AbortController() : null;
      var timer = ctrl ? setTimeout(function () { ctrl.abort(); }, 12000) : null;
      fetch("/.netlify/functions/settings", { cache: "no-store", signal: ctrl ? ctrl.signal : undefined })
        .then(function (r) { if (timer) clearTimeout(timer); if (!r.ok) throw new Error("bad status"); return r.json(); })
        .then(function (p) {
          if (!p || typeof p !== "object") throw new Error("no settings");
          try { localStorage.setItem(PUBCACHE, JSON.stringify(p)); } catch (e) {}
          applyPub(p);
        })
        .catch(function () {
          if (timer) clearTimeout(timer);
          if (attempt < 2) { setTimeout(function () { tryLoad(attempt + 1); }, 2500); }
          // else: give up quietly — cached/published content stays; defaults are never restored.
        });
    }
    tryLoad(1);
  }

  /* ------------------------------------------------ custom lens cursor */
  function initCursor() {
    if (!canHover || window.innerWidth < 1024) return;
    var cursor = document.createElement("div");
    cursor.className = "lens-cursor";
    cursor.setAttribute("aria-hidden", "true");
    cursor.innerHTML = "<span>View</span>";
    document.body.appendChild(cursor);

    var x = 0, y = 0, cx = 0, cy = 0;
    document.addEventListener("mousemove", function (e) {
      x = e.clientX; y = e.clientY;
      document.body.classList.add("lens-active");
    });
    document.addEventListener("mouseleave", function () {
      document.body.classList.remove("lens-active");
    });
    (function loop() {
      cx += (x - cx) * 0.18;
      cy += (y - cy) * 0.18;
      cursor.style.left = cx + "px";
      cursor.style.top = cy + "px";
      requestAnimationFrame(loop);
    })();

    document.querySelectorAll("[data-cursor]").forEach(function (el) {
      var label = el.getAttribute("data-cursor") || "View";
      el.addEventListener("mouseenter", function () {
        cursor.querySelector("span").textContent = label;
        cursor.style.width = "104px";
        cursor.style.height = "104px";
        cursor.style.background = "rgba(170,206,195,0.2)";
      });
      el.addEventListener("mouseleave", function () {
        cursor.style.width = "80px";
        cursor.style.height = "80px";
        cursor.style.background = "rgba(170,206,195,0.10)";
      });
    });
  }

  /* ---------------------------------------------------- scroll reveal */
  function initReveal() {
    var els = document.querySelectorAll(".reveal-up");
    if (!els.length) return;
    if (reduceMotion || !("IntersectionObserver" in window)) {
      els.forEach(function (el) { el.classList.add("active"); });
      return;
    }
    var io = new IntersectionObserver(function (entries) {
      entries.forEach(function (entry) {
        if (entry.isIntersecting) {
          entry.target.classList.add("active");
          io.unobserve(entry.target);
        }
      });
    }, { threshold: 0.12, rootMargin: "0px 0px -40px 0px" });
    els.forEach(function (el) { io.observe(el); });
  }

  /* ------------------------------------------------ testimonial slider */
  function initTestimonials() {
    var slider = document.getElementById("testimonialSlider");
    if (!slider) return;
    var slides = slider.children;
    var total = slides.length;
    var current = 0;
    var dotsWrap = document.getElementById("testimonialDots");

    function render() {
      slider.style.transform = "translateX(-" + (current * 100) + "%)";
      if (dotsWrap) {
        Array.prototype.forEach.call(dotsWrap.children, function (d, i) {
          d.classList.toggle("bg-secondary", i === current);
          d.classList.toggle("bg-secondary/30", i !== current);
        });
      }
    }
    if (dotsWrap) {
      for (var i = 0; i < total; i++) {
        var dot = document.createElement("button");
        dot.className = "w-2.5 h-2.5 rounded-full transition-colors bg-secondary/30";
        dot.setAttribute("aria-label", "Go to testimonial " + (i + 1));
        (function (idx) { dot.addEventListener("click", function () { current = idx; render(); }); })(i);
        dotsWrap.appendChild(dot);
      }
    }
    var next = document.getElementById("nextTest");
    var prev = document.getElementById("prevTest");
    if (next) next.addEventListener("click", function () { current = (current + 1) % total; render(); });
    if (prev) prev.addEventListener("click", function () { current = (current - 1 + total) % total; render(); });

    var timer;
    function play() { if (!reduceMotion) timer = setInterval(function () { current = (current + 1) % total; render(); }, 6500); }
    function stop() { clearInterval(timer); }
    slider.parentElement.addEventListener("mouseenter", stop);
    slider.parentElement.addEventListener("mouseleave", play);
    render(); play();
  }

  /* ---------------------------------------------------- hero parallax */
  function initParallax() {
    if (reduceMotion) return;
    var el = document.getElementById("heroText");
    if (!el) return;
    window.addEventListener("scroll", function () {
      var s = window.pageYOffset;
      if (s > window.innerHeight) return;
      el.style.transform = "translateY(" + (s * 0.25) + "px)";
      el.style.opacity = Math.max(0, 1 - s / 650);
    }, { passive: true });
  }

  /* --------------------------------------------------- gallery filter */
  function initGalleryFilter() {
    var wrap = document.querySelector("[data-filterable]");
    if (!wrap) return;
    var buttons = wrap.querySelectorAll("[data-filter]");
    var items = wrap.querySelectorAll("[data-category]");
    buttons.forEach(function (btn) {
      btn.addEventListener("click", function () {
        var f = btn.getAttribute("data-filter");
        buttons.forEach(function (b) {
          b.classList.toggle("text-secondary", b === btn);
          b.classList.toggle("border-secondary", b === btn);
          b.classList.toggle("text-on-surface-variant", b !== btn);
          b.classList.toggle("border-transparent", b !== btn);
        });
        items.forEach(function (it) {
          var match = f === "all" || it.getAttribute("data-category") === f;
          it.style.display = match ? "" : "none";
        });
      });
    });
  }

  /* ----------------------------------------------------- form helpers */
  function showError(input, msg) {
    var holder = input.parentElement.querySelector(".field-error");
    if (holder) holder.textContent = msg || "";
    input.classList.toggle("invalid", !!msg);
    if (msg) input.setAttribute("aria-invalid", "true");
    else input.removeAttribute("aria-invalid");
  }
  function isEmail(v) { return /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(v); }

  function initBookingForm() {
    var forms = document.querySelectorAll("form[data-booking], #bookingForm");
    forms.forEach(function (form) {
    var status = form.querySelector("[data-form-status]") || document.getElementById("formStatus");

    form.addEventListener("submit", function (e) {
      e.preventDefault();
      var ok = true;
      var name = form.querySelector("[name=name]");
      var email = form.querySelector("[name=email]");
      var service = form.querySelector("[name=service]");
      var message = form.querySelector("[name=message]");

      if (name && !name.value.trim()) { showError(name, "Please tell us your name."); ok = false; } else if (name) showError(name, "");
      if (email && !isEmail(email.value.trim())) { showError(email, "Enter a valid email address."); ok = false; } else if (email) showError(email, "");
      if (service && !service.value) { showError(service, "Please choose a service."); ok = false; } else if (service) showError(service, "");
      if (message && message.value.trim().length < 10) { showError(message, "A little more detail, please (10+ characters)."); ok = false; } else if (message) showError(message, "");

      if (!ok) {
        if (status) { status.textContent = "Please correct the highlighted fields."; status.className = "mt-6 text-error text-sm"; }
        return;
      }
      var btn = form.querySelector("[type=submit]");
      if (btn) { btn.disabled = true; btn.dataset.label = btn.textContent; btn.textContent = "Sending…"; }

      // Submit to Netlify Forms (form-encoded POST to the site root).
      var payload = new URLSearchParams(new FormData(form)).toString();
      fetch("/", {
        method: "POST",
        headers: { "Content-Type": "application/x-www-form-urlencoded" },
        body: payload
      }).then(function (res) {
        if (res.ok) { window.location.href = "thank-you.html"; return; }
        throw new Error("submit failed");
      }).catch(function () {
        // Local preview / non-Netlify host: no backend to receive the POST.
        form.reset();
        if (btn) { btn.disabled = false; btn.textContent = btn.dataset.label || "Send Enquiry"; }
        if (status) {
          status.textContent = "Thank you — your enquiry has been received. (Deploy on Netlify to capture these by email.)";
          status.className = "mt-6 text-tertiary text-sm";
        }
      });
    });
    });
  }

  function initNewsletter() {
    document.querySelectorAll("[data-newsletter]").forEach(function (form) {
      form.addEventListener("submit", function (e) {
        e.preventDefault();
        var input = form.querySelector("input[type=email]");
        var msg = form.querySelector("[data-newsletter-msg]");
        if (!isEmail(input.value.trim())) {
          input.classList.add("invalid");
          if (msg) { msg.textContent = "Enter a valid email."; msg.style.color = "#ffb4ab"; }
          return;
        }
        input.classList.remove("invalid");
        if (msg) { msg.textContent = "Subscribed — welcome aboard."; msg.style.color = "#aacec3"; }
        form.reset();
      });
    });
  }

  /* ---------------------------------------------------- FAQ accordion */
  function initAccordion() {
    document.querySelectorAll("[data-accordion] > [data-acc-item]").forEach(function (item) {
      var btn = item.querySelector("[data-acc-trigger]");
      var panel = item.querySelector("[data-acc-panel]");
      var icon = item.querySelector("[data-acc-icon]");
      if (!btn || !panel) return;
      panel.style.maxHeight = "0px";
      btn.addEventListener("click", function () {
        var open = panel.style.maxHeight !== "0px" && panel.style.maxHeight !== "";
        panel.style.maxHeight = open ? "0px" : panel.scrollHeight + "px";
        btn.setAttribute("aria-expanded", String(!open));
        if (icon) icon.style.transform = open ? "rotate(0deg)" : "rotate(45deg)";
      });
    });
  }

  /* ---------------------------------------------------- scroll progress */
  function initScrollProgress() {
    var bar = document.createElement("div");
    bar.setAttribute("aria-hidden", "true");
    bar.style.cssText = "position:fixed;top:0;left:0;height:3px;width:0;background:var(--accent);z-index:60;transition:width .1s linear;will-change:width;";
    document.body.appendChild(bar);
    function upd() {
      var h = document.documentElement;
      var max = h.scrollHeight - h.clientHeight;
      bar.style.width = (max > 0 ? (h.scrollTop / max) * 100 : 0) + "%";
    }
    window.addEventListener("scroll", upd, { passive: true });
    window.addEventListener("resize", upd, { passive: true });
    upd();
  }

  /* ---------------------------------------------------- number counters */
  function easeOutCubic(t) { return 1 - Math.pow(1 - t, 3); }
  function initCounters() {
    var els = document.querySelectorAll("[data-count]");
    if (!els.length) return;
    function settle(el) { el.textContent = el.getAttribute("data-count") + (el.getAttribute("data-suffix") || ""); }
    if (reduceMotion || !("IntersectionObserver" in window)) { els.forEach(settle); return; }
    var io = new IntersectionObserver(function (entries) {
      entries.forEach(function (en) {
        if (!en.isIntersecting) return;
        io.unobserve(en.target);
        var el = en.target;
        var target = parseInt(el.getAttribute("data-count"), 10) || 0;
        var suf = el.getAttribute("data-suffix") || "";
        var dur = 1500, start = null;
        function step(ts) {
          if (!start) start = ts;
          var p = Math.min((ts - start) / dur, 1);
          el.textContent = Math.round(target * easeOutCubic(p)) + suf;
          if (p < 1) requestAnimationFrame(step); else el.textContent = target + suf;
        }
        requestAnimationFrame(step);
      });
    }, { threshold: 0.4 });
    els.forEach(function (el) { io.observe(el); });
  }

  /* ---------------------------------------------------- 3D tilt on hover */
  function initTilt() {
    if (reduceMotion || !canHover) return;
    document.querySelectorAll("[data-tilt]").forEach(function (el) {
      el.style.transition = "transform .25s cubic-bezier(0.16,1,0.3,1)";
      el.style.willChange = "transform";
      el.addEventListener("mousemove", function (e) {
        var r = el.getBoundingClientRect();
        var x = (e.clientX - r.left) / r.width - 0.5;
        var y = (e.clientY - r.top) / r.height - 0.5;
        el.style.transform = "perspective(1000px) rotateY(" + (x * 5).toFixed(2) + "deg) rotateX(" + (-y * 5).toFixed(2) + "deg) scale(1.01)";
      });
      el.addEventListener("mouseleave", function () { el.style.transform = ""; });
    });
  }

  /* ---------------------------------------------------- magnetic buttons */
  function initMagnetic() {
    if (reduceMotion || !canHover) return;
    document.querySelectorAll("[data-magnetic]").forEach(function (el) {
      el.style.transition = "transform .2s ease";
      el.addEventListener("mousemove", function (e) {
        var r = el.getBoundingClientRect();
        var x = e.clientX - r.left - r.width / 2;
        var y = e.clientY - r.top - r.height / 2;
        el.style.transform = "translate(" + (x * 0.25).toFixed(1) + "px," + (y * 0.35).toFixed(1) + "px)";
      });
      el.addEventListener("mouseleave", function () { el.style.transform = ""; });
    });
  }

  /* ----------------------------------------- managed text (admin) */
  function applyManagedText() {
    var ov = mergedText();
    function val(k) { return (ov[k] != null && ov[k] !== "") ? ov[k] : null; }
    document.querySelectorAll("[data-text]").forEach(function (el) {
      var v = val(el.getAttribute("data-text")); if (v != null) el.textContent = v;
    });
    document.querySelectorAll("[data-href-mail]").forEach(function (el) {
      var v = val(el.getAttribute("data-href-mail")); if (v) el.setAttribute("href", "mailto:" + v);
    });
    document.querySelectorAll("[data-href-tel]").forEach(function (el) {
      var v = val(el.getAttribute("data-href-tel")); if (v) el.setAttribute("href", "tel:" + v.replace(/[^+0-9]/g, ""));
    });
    document.querySelectorAll("[data-href-url]").forEach(function (el) {
      var v = val(el.getAttribute("data-href-url")); if (v) el.setAttribute("href", v);
    });
  }

  function init() {
    applyManagedImages();
    applyManagedText();
    applyGalleries();
    fetchPublished();
    initScrollProgress();
    initCounters();
    initTilt();
    initMagnetic();
    initCursor();
    initReveal();
    initTestimonials();
    initParallax();
    initGalleryFilter();
    initBookingForm();
    initNewsletter();
    initAccordion();
  }

  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", init);
  } else { init(); }
})();
